#!/usr/bin/env bash
# start_cron.sh — Optional scheduled volume backups for tools-ecards (dev)
#
# Backs up Docker named volumes: postgres_data, redis_data, cassandra_data
# Stops only the Postgres container briefly for a filesystem-consistent tarball.
#
# Usage: ./bin/start_cron.sh [dev]
# Requires: .env or .env.dev (same as bin/start.sh)

set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
PROJECT_ROOT="$(cd "$SCRIPT_DIR/.." && pwd)"

TARGET_ENV="${1:-}"

if [ -z "$TARGET_ENV" ]; then
  if [ -f "$PROJECT_ROOT/.env" ]; then
    TARGET_ENV="dev"
  elif [ -f "$PROJECT_ROOT/.env.dev" ]; then
    TARGET_ENV="dev"
  else
    echo "Usage: $0 dev"
    echo "Create .env from .env.dev.example (or .env.dev) first."
    exit 1
  fi
fi

TARGET_ENV=$(echo "$TARGET_ENV" | tr '[:upper:]' '[:lower:]')

if [ "$TARGET_ENV" != "dev" ]; then
  echo "Only 'dev' is supported for cron backups in this repo."
  exit 1
fi

if [ -f "$PROJECT_ROOT/.env" ]; then
  ENV_FILE="$PROJECT_ROOT/.env"
elif [ -f "$PROJECT_ROOT/.env.dev" ]; then
  ENV_FILE="$PROJECT_ROOT/.env.dev"
else
  echo "❌ No .env or .env.dev at $PROJECT_ROOT"
  exit 1
fi

if [ -d "/mnt/data" ]; then BACKUP_BASE="/mnt/data"; else BACKUP_BASE="/data"; fi

# Stack identity (must match docker-compose.dev.yml container_name and bin/start.sh)
TD_STACK_SUFFIX_VAL=""
if grep -qE '^[[:space:]]*TD_STACK_SUFFIX=' "$ENV_FILE" 2>/dev/null; then
  TD_STACK_SUFFIX_VAL=$(grep -E '^[[:space:]]*TD_STACK_SUFFIX=' "$ENV_FILE" | head -1 | cut -d= -f2- | tr -d '\r' | sed "s/^['\"]//;s/['\"]$//" | xargs)
fi
if [ -z "$TD_STACK_SUFFIX_VAL" ]; then
  TD_STACK_SUFFIX_VAL="_dev_tcrd"
  echo "WARN: TD_STACK_SUFFIX missing in $ENV_FILE — using _dev_tcrd" >&2
fi
if ! echo "$TD_STACK_SUFFIX_VAL" | grep -qE '^_dev_'; then
  echo "ERROR: cron backup supports dev only; set TD_STACK_SUFFIX like _dev_tcrd in $ENV_FILE" >&2
  exit 1
fi
TD_COMPOSE_ID="tools_dashboard${TD_STACK_SUFFIX_VAL}"

PROJ_NAME=""
if grep -qE '^[[:space:]]*COMPOSE_PROJECT_NAME=' "$ENV_FILE" 2>/dev/null; then
  PROJ_NAME=$(grep -E '^[[:space:]]*COMPOSE_PROJECT_NAME=' "$ENV_FILE" | head -1 | cut -d= -f2- | tr -d '\r' | sed "s/^['\"]//;s/['\"]$//" | xargs)
fi
if [ -n "$PROJ_NAME" ] && [ "$PROJ_NAME" != "$TD_COMPOSE_ID" ]; then
  echo "ERROR: COMPOSE_PROJECT_NAME must be tools_dashboard + TD_STACK_SUFFIX = ${TD_COMPOSE_ID} (fix $ENV_FILE)" >&2
  exit 1
fi
PROJ_NAME="$TD_COMPOSE_ID"

# Postgres: tools_dashboard${TD_STACK_SUFFIX}-postgres; override with POSTGRES_BACKUP_CONTAINER if needed
POSTGRES_CONTAINER="tools_dashboard${TD_STACK_SUFFIX_VAL}-postgres"
if grep -qE '^[[:space:]]*POSTGRES_BACKUP_CONTAINER=' "$ENV_FILE" 2>/dev/null; then
  POSTGRES_CONTAINER=$(grep -E '^[[:space:]]*POSTGRES_BACKUP_CONTAINER=' "$ENV_FILE" | head -1 | cut -d= -f2- | tr -d '\r' | sed "s/^['\"]//;s/['\"]$//" | xargs)
fi

VOL_PREFIX="${PROJ_NAME}_"
PG_VOLUME="${VOL_PREFIX}postgres_data"
REDIS_VOLUME="${VOL_PREFIX}redis_data"
CASSANDRA_VOLUME="${VOL_PREFIX}cassandra_data"
BACKUP_DIR="${BACKUP_BASE}/backups_cron_${PROJ_NAME}"
CRON_SCRIPT_PATH="${BACKUP_DIR}/stack_volume_backup.sh"
LOG_FILE="${BACKUP_DIR}/backup_cron.log"

GREEN='\033[0;32m'
NC='\033[0m'

echo "========================================="
echo "   tools-ecards — Cron backup helper"
echo "========================================="
echo "Project name:     $PROJ_NAME"
echo "Postgres container: $POSTGRES_CONTAINER"
echo "Volumes:          $PG_VOLUME / $REDIS_VOLUME / $CASSANDRA_VOLUME"
echo "Backup dir:       $BACKUP_DIR"
echo "Generated script: $CRON_SCRIPT_PATH"
echo "========================================="
echo ""

mkdir -p "$BACKUP_DIR"

# shellcheck disable=SC2016
cat > "$CRON_SCRIPT_PATH" <<EOF
#!/usr/bin/env bash
# Auto-generated by bin/start_cron.sh — do not edit by hand.
set -euo pipefail
timestamp=\$(date +%Y-%m-%d_%H-%M-%S)
backup_dir="$BACKUP_DIR"
log_file="$LOG_FILE"
pg_vol="$PG_VOLUME"
redis_vol="$REDIS_VOLUME"
cassandra_vol="$CASSANDRA_VOLUME"
pg_container="$POSTGRES_CONTAINER"

exec >>"\$log_file" 2>&1
echo "[\$timestamp] Starting cron volume backup"
docker stop "\$pg_container" 2>/dev/null || true

docker run --rm -v "\$redis_vol:/data" -v "\$backup_dir:/backup" busybox sh -c "tar czf /backup/backup_redis_\$timestamp.tar.gz -C /data ."
docker run --rm -v "\$pg_vol:/data" -v "\$backup_dir:/backup" busybox sh -c "tar czf /backup/backup_pg_\$timestamp.tar.gz -C /data ."
docker run --rm -v "\$cassandra_vol:/data" -v "\$backup_dir:/backup" busybox sh -c "tar czf /backup/backup_cassandra_\$timestamp.tar.gz -C /data ." || echo "[\$timestamp] WARN: Cassandra tar failed (volume empty or busy)"

docker start "\$pg_container" 2>/dev/null || true
echo "[\$timestamp] Backup complete"
EOF

chmod +x "$CRON_SCRIPT_PATH"

echo "1. Install cron job (daily 01:00)"
echo "2. Run backup now"
echo "3. Remove cron job"
echo "4. View last log lines"
echo "0. Exit"
read -r -p "Select: " opt

case "$opt" in
  1)
    (crontab -l 2>/dev/null | grep -vF "$CRON_SCRIPT_PATH" || true; echo "0 1 * * * $CRON_SCRIPT_PATH") | crontab -
    echo -e "${GREEN}✓ Cron installed${NC}"
    ;;
  2)
    "$CRON_SCRIPT_PATH"
    echo "Done."
    ;;
  3)
    crontab -l 2>/dev/null | grep -vF "$CRON_SCRIPT_PATH" | crontab - || true
    echo "Removed."
    ;;
  4)
    if [ -f "$LOG_FILE" ]; then tail -n 40 "$LOG_FILE"; else echo "No log yet."; fi
    ;;
  0) exit 0 ;;
  *) echo "Invalid option"; exit 1 ;;
esac
